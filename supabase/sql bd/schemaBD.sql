-- ---------------------------------------------------------------------------
-- Perfil: nome e tipo de acesso (login/senha ficam no Auth do Supabase)
-- ---------------------------------------------------------------------------
CREATE TABLE public.perfil (
  id uuid PRIMARY KEY REFERENCES auth.users (id) ON DELETE CASCADE,
  nome_completo text NOT NULL DEFAULT '',
  perfil_acesso text NOT NULL DEFAULT 'voluntario'
    CHECK (perfil_acesso IN ('administrador', 'voluntario', 'validador')),
  data_atualizacao timestamptz DEFAULT NOW()
);

COMMENT ON TABLE public.perfil IS 'Dados do usuário logado: nome exibido e papel no sistema.';

ALTER TABLE public.perfil ENABLE ROW LEVEL SECURITY;

CREATE POLICY perfil_ler_proprio
  ON public.perfil FOR SELECT
  USING (auth.uid() = id);

CREATE POLICY perfil_inserir_proprio
  ON public.perfil FOR INSERT
  WITH CHECK (auth.uid() = id);

CREATE POLICY perfil_atualizar_proprio
  ON public.perfil FOR UPDATE
  USING (auth.uid() = id)
  WITH CHECK (auth.uid() = id);

-- Quando nasce um usuário no Auth, cria a linha correspondente aqui
CREATE FUNCTION public.fn_novo_usuario_cria_perfil()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  meta_perfil text;
  perfil_escolhido text;
BEGIN
  meta_perfil := lower(trim(COALESCE(new.raw_user_meta_data->>'perfil_acesso', '')));
  perfil_escolhido := CASE
    WHEN meta_perfil IN ('voluntario', 'validador') THEN meta_perfil
    ELSE 'voluntario'
  END;

  INSERT INTO public.perfil (id, nome_completo, perfil_acesso)
  VALUES (
    new.id,
    COALESCE(NULLIF(trim(new.raw_user_meta_data->>'nome_completo'), ''), ''),
    perfil_escolhido
  );

  RETURN new;
END;
$$;

CREATE TRIGGER ao_cadastrar_usuario
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE PROCEDURE public.fn_novo_usuario_cria_perfil();

-- Só administrador pode mudar o campo perfil_acesso (ninguém vira admin sozinho pelo app)
CREATE FUNCTION public.fn_so_admin_muda_perfil()
RETURNS trigger
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  -- Com usuário logado no app: só administrador altera perfil_acesso.
  -- No SQL Editor do Supabase, auth.uid() costuma ser NULL (sem JWT): aí não bloqueia,
  -- para você poder promover o primeiro admin com um UPDATE manual.
  IF tg_op = 'UPDATE' AND new.perfil_acesso IS DISTINCT FROM old.perfil_acesso THEN
    IF auth.uid() IS NOT NULL AND NOT EXISTS (
      SELECT 1 FROM public.perfil p
      WHERE p.id = auth.uid() AND p.perfil_acesso = 'administrador'
    ) THEN
      RAISE EXCEPTION 'Apenas administrador pode alterar o perfil de acesso';
    END IF;
  END IF;
  RETURN new;
END;
$$;

CREATE TRIGGER antes_de_atualizar_perfil
  BEFORE UPDATE ON public.perfil
  FOR EACH ROW EXECUTE PROCEDURE public.fn_so_admin_muda_perfil();

-- Quem já existia no Auth mas não tinha linha em perfil (útil após montar o banco)
INSERT INTO public.perfil (id, nome_completo, perfil_acesso)
SELECT u.id, '', 'voluntario'
FROM auth.users u
WHERE NOT EXISTS (SELECT 1 FROM public.perfil p WHERE p.id = u.id);

