-- Animais, adoções, eventos
-- ---------------------------------------------------------------------------
CREATE TABLE public.animal (
  id_animal bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome_animal text NOT NULL,
  raca text,
  foto text,
  descricao_historia text,
  castrado boolean NOT NULL DEFAULT false,
  vacinacao text,
  localizacao text,
  disponibilidade text NOT NULL,
  observacoes text,
  sexo text,
  porte text,
  data_nascimento date,
  data_cadastro timestamptz NOT NULL DEFAULT NOW()
);

CREATE TABLE public.adocao (
  id_operacao bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  data_operacao timestamptz NOT NULL DEFAULT NOW(),
  termo_adocao text NOT NULL,
  id_usuario uuid NOT NULL REFERENCES auth.users (id) ON DELETE RESTRICT,
  id_animal bigint NOT NULL REFERENCES public.animal (id_animal) ON DELETE RESTRICT,
  UNIQUE (id_animal)
);

CREATE INDEX idx_adocao_usuario ON public.adocao (id_usuario);

CREATE TABLE public.evento (
  id_evento bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tipo_evento text NOT NULL,
  data_evento timestamptz NOT NULL DEFAULT NOW(),
  id_usuario uuid NOT NULL REFERENCES auth.users (id) ON DELETE CASCADE,
  id_animal bigint NOT NULL REFERENCES public.animal (id_animal) ON DELETE CASCADE
);

CREATE INDEX idx_evento_usuario ON public.evento (id_usuario);
CREATE INDEX idx_evento_animal ON public.evento (id_animal);

ALTER TABLE public.animal ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.adocao ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.evento ENABLE ROW LEVEL SECURITY;

